#ifndef fibonacci_h
#define fibonacci_h

int fibonacci(int n) {
    if (n == 0) return 0;
    if (n == 1) return 1;
    return fibonacci(n - 1) + fibonacci(n - 2);
}

#endif /* fibonacci_h */
